
#include <iostream>
using namespace std;
int main()
{
    cout <<" X + Y = summation result\n" <<
        "X * Y = multiplication result \n" <<
        "X - Y = subtraction result\n";
    long long int fn, ln;
    cin >> fn>>ln;
    cout<< fn <<" + "<< ln<<" = " <<  fn + ln << endl;
    cout<< fn <<" * "<< ln<<" = " <<  fn * ln << endl;
    cout<< fn <<" - "<< ln<<" = " <<  fn - ln << endl;
}
